create table if not exists public.redirect_rules (
  id bigint generated by default as identity primary key,
  slug text not null unique,
  target_url text not null,
  status_code integer not null default 302 check (status_code in (301, 302)),
  is_active boolean not null default true,
  pixel_enabled boolean not null default false,
  pixel_type text check (pixel_type in ('meta', 'tiktok', 'google', 'postback')),
  pixel_config jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint redirect_rules_slug_format check (slug ~ '^[a-z0-9][a-z0-9_-]{0,119}$'),
  constraint redirect_rules_pixel_consistency check (
    (pixel_enabled = false and pixel_type is null and pixel_config is null)
    or (pixel_enabled = true and pixel_type is not null and pixel_config is not null)
  )
);

create table if not exists public.logs (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  rule_id bigint references public.redirect_rules(id) on delete set null,
  slug text not null,
  target_url text not null,
  status_code integer not null check (status_code in (301, 302)),
  ip_hash text not null,
  user_agent text,
  referer text,
  referer_domain text not null default 'direct',
  country text not null default 'UNK',
  device_type text not null default 'unknown' check (device_type in ('mobile', 'desktop', 'bot', 'unknown')),
  query_string text not null default ''
);

create table if not exists public.pixel_logs (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  rule_id bigint references public.redirect_rules(id) on delete set null,
  slug text not null,
  pixel_type text not null check (pixel_type in ('meta', 'tiktok', 'google', 'postback')),
  event_name text not null,
  status text not null check (status in ('success', 'error')),
  response_code integer,
  response_body text,
  error_message text,
  payload jsonb
);

