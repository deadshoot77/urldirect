create or replace function public.stats_clicks_by_slug(p_limit integer default 100)
returns table (label text, clicks bigint)
language sql
security definer
set search_path = public
as $$
  select
    l.slug::text as label,
    count(*)::bigint as clicks
  from public.logs l
