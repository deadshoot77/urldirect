create or replace function public.resolve_redirect(
  p_slug text,
  p_default_url text,
  p_default_status integer,
  p_user_agent text,
  p_referer text,
  p_ip_hash text,
  p_country text,
  p_query_string text
)
returns table (
  resolved_slug text,
  target_url text,
  status_code integer,
  pixel_enabled boolean,
  pixel_type text,
  pixel_config jsonb,
  rule_id bigint
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_slug text;
  v_target_url text;
  v_status_code integer;
  v_pixel_enabled boolean := false;
  v_pixel_type text := null;
  v_pixel_config jsonb := null;
  v_rule_id bigint := null;
  v_referer_domain text := 'direct';
  v_device_type text := 'unknown';
begin
  v_slug := lower(btrim(coalesce(p_slug, '')));
  if v_slug = '' then
    v_slug := '/';
  end if;

  if v_slug <> '/' then
    select
      rr.id,
      rr.target_url,
      rr.status_code,
      rr.pixel_enabled,
      rr.pixel_type,
      rr.pixel_config
    into
      v_rule_id,
      v_target_url,
      v_status_code,
      v_pixel_enabled,
      v_pixel_type,
      v_pixel_config
    from public.redirect_rules rr
    where rr.slug = v_slug
      and rr.is_active = true
    order by rr.id desc
    limit 1;
  end if;

  if v_rule_id is null then
    v_target_url := p_default_url;
    v_status_code := case when p_default_status in (301, 302) then p_default_status else 302 end;
    v_pixel_enabled := false;
    v_pixel_type := null;
    v_pixel_config := null;
  end if;

  if coalesce(p_referer, '') <> '' then
    v_referer_domain := lower(split_part(split_part(split_part(p_referer, '://', 2), '/', 1), '?', 1));
    if coalesce(v_referer_domain, '') = '' then
      v_referer_domain := 'direct';
    end if;
  end if;

  if coalesce(p_user_agent, '') ~* '(bot|crawler|spider|crawling|headless|preview)' then
    v_device_type := 'bot';
  elsif coalesce(p_user_agent, '') ~* '(android|iphone|ipad|ipod|mobile|windows phone)' then
    v_device_type := 'mobile';
  elsif coalesce(p_user_agent, '') = '' then
    v_device_type := 'unknown';
  else
    v_device_type := 'desktop';
  end if;

  insert into public.logs (
    rule_id,
    slug,
    target_url,
    status_code,
    ip_hash,
    user_agent,
    referer,
    referer_domain,
    country,
    device_type,
    query_string
  ) values (
    v_rule_id,
    v_slug,
    v_target_url,
    v_status_code,
    p_ip_hash,
    nullif(p_user_agent, ''),
    nullif(p_referer, ''),
    v_referer_domain,
    coalesce(nullif(upper(p_country), ''), 'UNK'),
    v_device_type,
    coalesce(p_query_string, '')
  );

  return query
  select
    v_slug,
    v_target_url,
    v_status_code,
    v_pixel_enabled,
    v_pixel_type,
    v_pixel_config,
    v_rule_id;
end;
$$;

