$$;

create or replace function public.stats_daily(p_days integer default 30)
returns table (day text, clicks bigint)
language sql
security definer
set search_path = public
as $$
  with bounds as (
    select greatest(coalesce(p_days, 30), 1) as days
  ),
  series as (
    select generate_series(
      current_date - ((select days from bounds) - 1),
      current_date,
      interval '1 day'
    )::date as day
  ),
  agg as (
    select
      date_trunc('day', l.created_at)::date as day,
      count(*)::bigint as clicks
    from public.logs l
    where l.created_at >= (current_date - ((select days from bounds) - 1))::timestamp
    group by 1
  )
  select
    to_char(s.day, 'YYYY-MM-DD') as day,
    coalesce(a.clicks, 0)::bigint as clicks
  from series s
  left join agg a on a.day = s.day
  order by s.day asc;
$$;

create or replace function public.stats_hourly(p_days integer default 30)
returns table (hour text, clicks bigint)
language sql
security definer
set search_path = public
as $$
  with bounds as (
    select greatest(coalesce(p_days, 30), 1) as days
  ),
  hours as (
    select generate_series(0, 23) as hour_num
  ),
  agg as (
    select
      extract(hour from l.created_at)::integer as hour_num,
      count(*)::bigint as clicks
    from public.logs l
    where l.created_at >= now() - make_interval(days => (select days from bounds))
    group by 1
  )
  select
    lpad(h.hour_num::text, 2, '0') as hour,
    coalesce(a.clicks, 0)::bigint as clicks
  from hours h
  left join agg a on a.hour_num = h.hour_num
  order by h.hour_num asc;
$$;

